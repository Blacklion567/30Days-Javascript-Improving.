//*indexOf(): Takes takes a substring and if the substring exists in a string it returns the first position of the substring if does not exist it returns -1
let string = 'Programming Is The Best'
console.log(string.indexOf('T'))   // 15
console.log(string.indexOf('Best'))   // 19
console.log(string.indexOf('t'))   // 22
console.log(string.indexOf('g'))   // 3
console.log(string.indexOf('m'))   // 6
console.log(string.indexOf('Is'))   // 12
console.log(string.indexOf('The'))   // 15
console.log(string.indexOf('z'))   // -1



