// toUpperCase(): this method changes the string to uppercase letters.

let string = 'Jade Ivan V. BrINgcola'
console.log(string.toUpperCase())       //JADE IVAN V. BRINGCOLA

let codeName = 'bandev'
console.log(codeName.toUpperCase())     //BANDEV

let country = 'philippines'
console.log(country.toUpperCase())      //PHILIPPINES