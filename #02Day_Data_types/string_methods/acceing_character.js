// Let us access the first character in 'Jade Ivan' string.
let firstName = 'Jade Ivan'
let secondLetter = firstName[0]
console.log(secondLetter)   //J

// You Can Make A More If You Want To Until You Reach It's Maximum Value.
let lastLetter = firstName[8]
console.log(lastLetter)     //n


let stringLength = firstName.length // Length means how many Value inside the string SPACE is counted as well.  
console.log(stringLength)


let lastIndex = firstName.length -1 //This is how you access the last value inside of a string by using negative number. 
console.log(lastIndex) // 8
console.log(firstName[lastIndex]) // n
