// toLowerCase(): this method changes the string to lowercase letters.

let string = 'JADE Ivan V. BrINgcola'
console.log(string.toLowerCase())       //jade ivan v. bringcola

let codeName = 'BanDEv'
console.log(codeName.toLowerCase())     //bandev

let country = 'PHILIPpINes'
console.log(country.toLowerCase())      //philippines