//substr(): It takes two arguments,the starting index and number of characters to slice.

let string = 'I Really Love Programming'
console.log(string.substr(2,7))     //Really
console.log(string.substr(9,4))     //Love
console.log(string.substr(14))      //Programming
console.log(string.substr(2,11))    //Really Love
console.log(string.substr(0,1))     //I

let country = 'Philippines'
console.log(country.substr(0,11))   //Philippines
console.log(country.substr(6))      //pines
console.log(country.substr(4,3))    //ipp