// length: The string length method returns the number of characters in a string included empty space. Example:

let js = 'Javascript'
console.log(js.length)              //10

let py = 'Python'
console.log(py.length)              //6

let cpp = 'C++'
console.log(cpp.length)             //3

let programming = 'Language'
console.log(programming.length)     //8

let firstName = 'Jade Ivan'
console.log(firstName.length)      //9

let address = 'Live In Bagacay Sibonga Cebu'
console.log(address.length)        //28