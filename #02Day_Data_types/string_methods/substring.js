// substring(): It takes two arguments,the starting index and the stopping index but it doesn't include the stopping index.

let string = 'I Really Love Programming'
console.log(string.substring(0,1))    //I
console.log(string.substring(2,8))    //Really
console.log(string.substring(9,13))   //Love
console.log(string.substring(14))     //Programming1
console.log(string.substring(0,13))   //I Really Love


let country = 'Philippines'
console.log(country.substring(0,3))   //Phi
console.log(country.substring(3,5))   //li
console.log(country.substring(6))     //pines
